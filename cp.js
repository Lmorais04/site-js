let opção = parseInt(prompt("digite uma opção"));

switch(opção){
    case 1:
        document.write("primeira opção")
        break;
    case 2:
        document.write("segunda opção")
        break;
    case 3:
        document.write("terceira opção")
        break;
    default:
        document.write("caso nao seja a primeira opção e nem a segunda opção")
        break;
}